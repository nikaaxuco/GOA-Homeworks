// - spread იღებს უკვე არსებულ მასივს ან ობიექტს და „იგრძნობს“ მათ ელემენტებად.
// - rest იხმარება ფუნქციის პარამეტრებში ან დესტრუქტურინგში, რათა „გადაიღოს“ 
//   დანარჩენი ელემენტები ერთ მასივში ან ობიექტში.

function sum(...numbers) {
  return numbers.reduce((a, b) => a + b, 0);
}
