let subject1 = 95; 
let subject2 = 75; 
let subject3 = 60; 

let averageGrade = (subject1 + subject2 + subject3) / 3;
console.log("average grade:", averageGrade);

if (averageGrade >= 90) {
    console.log("A");
} else if (averageGrade >= 70) {
    console.log("B");
} else if (averageGrade >= 50) {
    console.log("C");
} else if (averageGrade >= 25) {
    console.log("D");
} else {
    console.log("წადი ისწავლე და მერე მოდი");
}


